using System;

// Да се напише if-конструкция, която проверява стойността на две целочислени променливи и разменя техните стойности, ако стойността на първата променлива е по-голяма от втората.

class Program
{
    static void Main()
    {
        int a = 64;
        int b = 32;
        int swap;

        if (a > b)
        {
            swap = a;
            a = b;
            b = swap;
        }
    }
}

